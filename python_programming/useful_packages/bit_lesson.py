# 論理和
print(0 | 0)
print(0 | 1)
print(1 | 0)
print(1 | 1)
# 論理積
print('論理積')
print(0 & 0)
print(0 & 1)
print(1 & 0)
print(1 & 1)

# 排他的論理和
print('排他的論理積')
print(0 ^ 0)
print(0 ^ 1)
print(1 ^ 0)
print(1 ^ 1)
# 反転
print('反転')
print(bin(0))
print(bin(~0))
print(bin(1))
print(bin(~1))
# シフト
print('シフト')
print(bin(1 << 0))
print(bin(1 << 1))
print(bin(1 << 2))
print(bin(5 << 1))
